package b2j;


public enum OptionConst {
    /**
     * More pretty printing, it's especially useful when you works on developing phase
     */
    PRETTY_PRINTING,
    /**
     * Make json string more readable. It's also recommend to set it.
     */
    MORE_READABLE,

    /**
     * Ignore the attributes of class file
     */
    IGNORE_CLASS_FILE_ATTRIBUTES,

    /**
     * Ignore methods
     */
    IGNORE_METHODS,

    /**
     * Ignore fields
     */
    IGNORE_FIELDS,

    /**
     * Ignore interfaces
     */
    IGNORE_INTERFACES,

    /**
     * Ignore constant pool slots
     */
    IGNORE_CONSTANT_POOL,
}
